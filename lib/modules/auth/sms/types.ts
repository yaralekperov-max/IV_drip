/**
 * Абстракция отправки SMS. Конкретный российский провайдер (SMS.ru, SMSC, SMS Aero и т.п.)
 * подключается реализацией этого интерфейса — код приложения от провайдера не зависит.
 */
export interface SmsSender {
  /** Название провайдера (для логов/диагностики). */
  readonly name: string;
  /** Отправляет текст на номер. Бросает ошибку при неудаче. */
  send(phone: string, message: string): Promise<void>;
}
